local hyper = {"alt"}
local hyperShifted = {"cmd", "alt"}

hs.hotkey.bind(hyper, "Y", function()
  local win = hs.window.focusedWindow()
  local f = win:frame()

  f.x = f.x - 10
  f.y = f.y - 10
  win:setFrame(f)
end)

hs.hotkey.bind(hyper, "K", function()
